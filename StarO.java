import java.util.*;
class StarO{
    
    public static void main(String args[]){
        
        //print Stars using loops
        for(int i =1;i<=5; i++){
            
            for(int j=i;i<=5;j++){
                
                System.out.print("*");
                
            }
        System.out.println();
    
        }
       
    }    
} 
